#include "ft_2048.h"

void	reset_changed(t_square board[4][4])
{
	int		y;
	int		x;

	y = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			board[y][x].changed = 0;
			x++;
		}
		y++;
	}
}
