#include "ft_2048.h"

int		square_row_size(void)
{
	int		row;
	int		i;

	row = getmaxy(stdscr) - 4;
	i = 0;
	while ((row - i) % 4 != 0 && row - i != row)
		i++;
	return ((row - i) / 4);
}
