#include "ft_2048.h"

int		total_score(t_env *e)
{
	int		total;
	int		x;
	int		y;

	y = 0;
	total = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			total += e->board[y][x].value;
			x++;
		}
		y++;
	}
	return (total);
}
